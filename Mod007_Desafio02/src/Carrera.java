
public class Carrera {
	
	//Atributos
	private String nombre;
	private String sede;
	
	//Metodos
	Carrera(){
	}
	
	public String getNombre() {
		return nombre;
	}

	public void setNombre(String nombre) {
		this.nombre = nombre;
	}

	public String getSede() {
		return sede;
	}

	public void setSede(String sede) {
		this.sede = sede;
	}
}
